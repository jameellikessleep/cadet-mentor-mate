// Chatbot for asking questions about the current lesson
const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

Deno.serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages, lesson } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY not configured");

    const context = lesson ? `You have access to the following ACF cadet lesson currently being taught.

Title: ${lesson.title}
Aim: ${lesson.aim}
Objectives: ${(lesson.objectives || []).join("; ")}
Safety: ${(lesson.safety || []).join("; ")}
Equipment: ${(lesson.equipment || []).join("; ")}
Slides: ${(lesson.slides || []).map((s: any, i: number) => `(${i + 1}) ${s.title}: ${(s.bullets || []).join(" • ")}`).join("\n")}
` : "";

    const systemPrompt = `You are a helpful UK Army Cadet Force (ACF) teaching assistant. Answer cadets' and instructors' questions about the lesson clearly, accurately, and age-appropriately. Use British military terminology. Keep answers concise (2-5 sentences). If asked about something outside the lesson scope, still answer if safe and educational. Refuse anything unsafe or inappropriate for cadets.

${context}`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [{ role: "system", content: systemPrompt }, ...messages],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit reached." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI error", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error(e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
